package org.exquisite.protege.model.exception;

class GeneralDiagnosisException extends Exception {

    GeneralDiagnosisException(String arg0) {
        super(arg0);
    }

    GeneralDiagnosisException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    GeneralDiagnosisException(Throwable arg0) {
        super(arg0);
    }

    GeneralDiagnosisException() {
        super();
    }
}
