package evaluations.dxc.synthetic.model;

/**
 * @author Thomas
 *
 */
public class DXCComponentType {
	private String name;
	private String description;
	private String modesRef;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getModesRef() {
		return modesRef;
	}

	public void setModesRef(String modesRef) {
		this.modesRef = modesRef;
	}
	
}
